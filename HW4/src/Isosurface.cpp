#include <Isosurface.hpp>

Isosurface::Isosurface(const VolumeData* data, int isovalue)
{
    this->data1 = new VolumeData(data);
    this->isovalue = isovalue;
    
    this->shader = new Shader("src/Shaders/isosurface.vert", "src/Shaders/isosurface.frag");
    this->VAO = new unsigned int[1];
    this->VBO = new unsigned int[1];
    glGenVertexArrays(1, VAO);
    glGenBuffers(1, VBO);
    this->vertices.clear();
    
    this->texture = NULL;
    this->texture3D = NULL;
    this->texture2D = NULL;
    this->texture1D = NULL;
}

Isosurface::~Isosurface()
{
    delete data1;
    delete shader;
    delete VAO;
    delete VBO;
}

void Isosurface::makeVertices()
{
    marchingCube();
}

void Isosurface::bindVertices()
{
    glBindVertexArray(VAO[0]);
    glBindBuffer(GL_ARRAY_BUFFER, VBO[0]);

    glBufferData(GL_ARRAY_BUFFER, sizeof(float) * vertices.size(), vertices.data(), GL_STATIC_DRAW);

    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 6 * sizeof(float), (void*)0);
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 6 * sizeof(float), (void*)( 3 * sizeof(float) ));
    glEnableVertexAttribArray(1);
}

void Isosurface::draw(glm::mat4 projection, glm::mat4 view, const vector<float> clipping, bool makeCrossSection, float gap, float adjust, float threshold)
{
    shader->use();

    shader->setMatrix4("projection", glm::value_ptr(projection));
    shader->setMatrix4("view", glm::value_ptr(view));

    glm::mat4 model = glm::mat4(1.0f);
    model = glm::scale(model, glm::vec3(data1->voxelSize.x, data1->voxelSize.y, data1->voxelSize.z));
    shader->setMatrix4("model", glm::value_ptr(model));

    shader->setFloatVec("clipping", clipping, 4);
    shader->setBool("makeCrossSection", makeCrossSection);

    glDisable(GL_CULL_FACE);

    glBindVertexArray(VAO[0]);
    glPolygonMode(GL_FRONT_AND_BACK, GL_FILL);
    glDrawArrays(GL_TRIANGLES, 0, vertices.size() / 6);
}

void Isosurface::setShader()
{
    delete this->shader;
    this->shader = new Shader("src/Shaders/isosurface.vert", "src/Shaders/isosurface.frag");
}

void Isosurface::marchingCube()
{
    vertices.clear();

    for(int x = 0; x < data1->resolution.x - 1; x++)
    for(int y = 0; y < data1->resolution.y - 1; y++)
    for(int z = 0; z < data1->resolution.z - 1; z++)
    {
        marchSingleCube(x, y, z);
    }

    bindVertices();
}

void Isosurface::marchSingleCube(int x, int y, int z)
{
    int compareWithIsovalue = 0;

    for(int i = 0; i < 8; i++)
    {
        int xoffset = offsetFromBaseVertex[i][0];
        int yoffset = offsetFromBaseVertex[i][1];
        int zoffset = offsetFromBaseVertex[i][2];

        if( data1->value[x + xoffset][y + yoffset][z + zoffset] > isovalue )
        {
            compareWithIsovalue |= (1 << i);
        }
    }

    vector<int> matchOfTable = lookUpTable[compareWithIsovalue];

    for(int i = 0; matchOfTable[i] != -1; i += 3 )
    {
        setVertices( { x, y, z },
                     { matchOfTable[i], matchOfTable[i + 1], matchOfTable[i + 2] });
    }
}

void Isosurface::setVertices(vector<int> baseVertex, vector<int> interEdges)
{
    glm::vec3 base;
    glm::vec3 direction;
    float ratio;
    
    float x0, y0, z0, x1, y1, z1;

    for(auto edge: interEdges)
    {
        x0 = baseVertex[0] + offsetFromBaseVertex[vertexOfEdges[edge][0]][0];
        y0 = baseVertex[1] + offsetFromBaseVertex[vertexOfEdges[edge][0]][1];
        z0 = baseVertex[2] + offsetFromBaseVertex[vertexOfEdges[edge][0]][2];

        x1 = baseVertex[0] + offsetFromBaseVertex[vertexOfEdges[edge][1]][0];
        y1 = baseVertex[1] + offsetFromBaseVertex[vertexOfEdges[edge][1]][1];
        z1 = baseVertex[2] + offsetFromBaseVertex[vertexOfEdges[edge][1]][2];

        base = glm::vec3(x0, y0, z0);
        direction = glm::vec3( x1-x0, y1-y0, z1-z0 );
        ratio = (isovalue - data1->value[x0][y0][z0]) / (data1->value[x1][y1][z1] - data1->value[x0][y0][z0]);

        vertices.push_back(base.x + ratio * direction.x - data1->resolution.x / 2.0f);
        vertices.push_back(base.y + ratio * direction.y - data1->resolution.y / 2.0f);
        vertices.push_back(base.z + ratio * direction.z - data1->resolution.z / 2.0f);

        glm::vec3 grad = glm::vec3(data1->gradient[x0][y0][z0][0] * (1.0f-ratio) +
                                   data1->gradient[x1][y1][z1][0] * ratio,
                                   data1->gradient[x0][y0][z0][1] * (1.0f-ratio) +
                                   data1->gradient[x1][y1][z1][1] * ratio,
                                   data1->gradient[x0][y0][z0][2] * (1.0f-ratio) +
                                   data1->gradient[x1][y1][z1][2] * ratio);
        
        grad = glm::normalize(grad);

        vertices.push_back(grad.x);
        vertices.push_back(grad.y);
        vertices.push_back(grad.z);
    }
}

void Isosurface::setIsovalue(float isovalue)
{
    this->isovalue = isovalue;
}

Shader* Isosurface::getShader() const
{
    return shader;
}

